package basic;

public class Loops {

	public static void main(String[] args) {
		int i;
		//For Loops
		for(i=0;i<5;i++) {
			System.out.println("Hi-FOR");
		}
	    //While Loops
		int j=0;
		while(j<10) {
			System.out.println("Hello-While");
			j++;
		}
		//Do-While Loops
		int k=0;
		do {
			System.out.println("How-DO_While");
			k++;
		}while(k<5);
		
     
	}

}
